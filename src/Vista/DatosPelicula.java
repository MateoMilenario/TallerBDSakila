    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        int Respuesta = JOptionPane.showConfirmDialog(null, "¿Está seguro/a que desea modificar los datos de la película");

        if (Respuesta == 0) {

            String nombrePelicula = NombreP.getText();
            String Añorealizacion = AñoRealizacion.getText();
            String CostoReemplazo = CostoR.getText();
            String duracionPelicula = DuracionP.getText();
            String tarifaR = TarifaRenta.getText();
            String duracionRenta = DuracionR.getText();
            String clasificacion = "";
            String caracteristica = jComboBoxCaracteristicas.getSelectedItem().toString();
            String idiomaP = ComboBoxIdioma.getSelectedItem().toString();
            String Descripcion = descripcionP.getText();

            //*****************************************************************************
            //Se válida los campos 
            if (jRadioPG.isSelected()) {

                clasificacion = "PG";

            } else if (jRadioNC17.isSelected()) {

                clasificacion = "NC-17";

            } else if (jRadioG.isSelected()) {

                clasificacion = "G";

            } else if (jRadioPG13.isSelected()) {

                clasificacion = "PG-13";

            } else if (jRadioR.isSelected()) {

                clasificacion = "R";

            }//Fin if

            if (clasificacion.equals("")) {

                clasificacion = clasificacionActual.getText();

            }//Fin if

            if (caracteristica.equals("Características")) {

                caracteristica = CaracteristicaActual.getText();

            }//Fin if

            if (idiomaP.equals("Lenguajes")) {

                idiomaP = IdiomaActual.getText();

            } else {

                String pKidioma = EncontrarPKidioma(idiomaP);
                DatosPelicula.get(contadorPosición).setFkIdioma(pKidioma);

            }//Fin if

            //*****************************************************************************
            //Se actualizan los datos de esa película que están guardados en el linkedList
            DatosPelicula.get(contadorPosición).setNombrePelicula(nombrePelicula);
            DatosPelicula.get(contadorPosición).setAñoCreacion(Añorealizacion);
            DatosPelicula.get(contadorPosición).setCostoReemplazo(CostoReemplazo);
            DatosPelicula.get(contadorPosición).setDuracionPelicula(duracionPelicula);
            DatosPelicula.get(contadorPosición).setPrecioRenta(tarifaR);
            DatosPelicula.get(contadorPosición).setDuracionRenta(duracionRenta);
            DatosPelicula.get(contadorPosición).setClasificacion(clasificacion);
            DatosPelicula.get(contadorPosición).setCaracteristicas(caracteristica);
            DatosPelicula.get(contadorPosición).setDescripcion(Descripcion);

            //Se llama este método para que actualice los campos que hay en la interfaz, siempre y cuando se haya presionado el botón modificar
            CrearCamposDatosPelicula();

            ControlPelicula cPelicula = new ControlPelicula();

            String PkPelicula = DatosPelicula.get(contadorPosición).getPkPelicula();

            boolean t = cPelicula.ModificarDatosPelicula(PkPelicula, DatosPelicula.get(contadorPosición));

            if (t) {

                JOptionPane.showMessageDialog(null, "Datos de la película modificados.");
                jComboBoxCaracteristicas.setSelectedItem("Características");
                ComboBoxIdioma.setSelectedItem("Lenguajes");

            }//Fin if

        }//Fin if confirmación

    }//GEN-LAST:event_jButton5ActionPerformed

