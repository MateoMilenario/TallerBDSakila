    public String ValidarIdiomaPelicula(String pkPelicula) {

        String IdiomaP = "";

        for (int i = 0; i < LenguajesRegistrados.size(); i++) {

            if (LenguajesRegistrados.get(i).getPkIdioma().equals(pkPelicula)) {

                IdiomaP = LenguajesRegistrados.get(i).getNombreIdioma();

            }//Fin if           

        }//Fin for

        return IdiomaP;

    }//Fin método

    public String EncontrarPKidioma(String idiomaP) {

        String pkIdioma = "";

        for (int i = 0; i < LenguajesRegistrados.size(); i++) {

            if (LenguajesRegistrados.get(i).getNombreIdioma().equals(idiomaP)) {

                pkIdioma = LenguajesRegistrados.get(i).getPkIdioma();

            }//Fin if

        }//Fin for

        return pkIdioma;

    }//Fin método

